# Функция с анимацией построчной печати
function print_animated_line() {
  local line="$1"
  for ((i=0; i<${#line}; i++)); do
    echo -ne "${line:$i:1}"
    sleep 0.005
  done
  echo
}

# Обновлённая функция: весь текст и "█" — пурпурным
function print_colored() {
  local m=$'\033[35m'
  local r=$'\033[0m'
  print_animated_line "${m}$1${r}"
}

